package Operators;

public class Diff_AND_OR {

	public static void main(String[] args) {

		boolean a=true,b=false;
		
		System.out.println(a&b); //false
		System.out.println(a|b); //true
		
		
		System.out.println(a&&b); //false
		System.out.println(a||b); //true
		
		
		int x=5,y=3;
				
		System.out.println(x&y); //false
		System.out.println(x|y); //true
		
		System.out.println(x&&y); //false
		System.out.println(x||y); //true
		
	}

}
